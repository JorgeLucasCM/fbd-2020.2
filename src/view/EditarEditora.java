    private void preencherCampos(){
        jTFNome.setText(main.editoraEditar.getNome());
        jTFCnpj.setText(main.editoraEditar.getCnpj());
        jTFCidade.setText(main.editoraEditar.getCidade());
    }
    
    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        if( jTFCidade.getText().equals("") || jTFNome.getText().equals("") || jTFCnpj.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!",null,JOptionPane.ERROR_MESSAGE);
            if(jTFCnpj.getText().equals(""))
                jTFCnpj.setBackground(new Color(255,102,102));
            if(jTFCidade.getText().equals(""))
                jTFCidade.setBackground(new Color(255,102,102));
            if(jTFNome.getText().equals(""))
                jTFNome.setBackground(new Color(255,102,102));
            if(jTFCnpj.getText().equals(""))
                jTFCnpj.setBackground(new Color(255,102,102));
        }else{

            Connection con = Conexao.AbrirConexao();
            
            
            
            try {
                if(new EditoraDAO(con).testarEditoraNome(main.editoraEditar.getNome()) && !(main.editoraEditar.getNome().equalsIgnoreCase(jTFNome.getText()))){
                    JOptionPane.showMessageDialog(null, "Nome j√° utilizado!",null,JOptionPane.ERROR_MESSAGE);
                }else{
                    main.editoraEditar.setNome(jTFNome.getText());
                    main.editoraEditar.setCidade(jTFCidade.getText());
                    new EditoraDAO(con).alterarEditora(main.editoraEditar);
                    JOptionPane.showMessageDialog(null, "Editora Cadastrada com sucesso");
                    new TelaBibliotecario().setVisible(true);
                    this.dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(CadastroAluno.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        }
    }//GEN-LAST:event_jBEditarActionPerformed

